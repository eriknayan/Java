
public class Exemplo1 {

	public static void main(String[] args) {
		int i = 7;
		float f = 9.7F;
		boolean b = true;
		char c = 'A';
		System.out.println("Valor do i: " + i);
		System.out.println("Valor do f: " + f);
		System.out.println("Valor do b: " + b);
		System.out.println("Valor do c: " + c);
		System.out.println("Maior int possivel: " + Integer.MAX_VALUE);
		System.out.println("Menor int possivel: " + Integer.MIN_VALUE);
		System.out.println("i em binario " + Integer.toBinaryString(i));
	}
}
